/*
     JavaScript Assignment Operators
*/

// = symbol is called assignment operator
let score = 100;
console.log(score);

// += operator

let number = 300;
// number = number + 100;
number += 100;
console.log(number);

// -= operator

let balance = 90;
// balance = balance - 10;
balance -= 10;
console.log(balance);

// *= operator
let donation = 7;
// donation = donation * 2;
donation *= 2;
console.log(donation);

// /= operator
let sweets = 10;
// sweets = sweets / 5;
sweets /= 5;
console.log(sweets);

// %= operator
let pizziSlices = 10;
// pizziSlices = pizziSlices % 3;
pizziSlices %= 3;
console.log(pizziSlices);

